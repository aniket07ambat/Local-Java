class Hello
{
    public static void main(String a[])
    {
        System.out.println("Hello World!");
        System.out.println(2 + 5);

        int num3 = 34;
        System.out.println(num3);

        // In java double is default SO You had to explicitly define it.
        // float num = 3.4; // Give error.
        float num = 3.4f;  

        System.out.println(num);
        double num2 = 34.34;
        System.out.println(num2);

        // Java does not used 0 or 1 to boolean data type. 
        // You have to use true or false for Boolean data type.
        boolean num5 = true;

        // Data types [primitive]

        int num6 = 3;
        byte by = 23;
        short num7 = 214;
        long num8 = 2134l; // You had to write l at the end of long

        float num9 = 34.4f;
        double num10 = 342.4;

        boolean num11 = true;
        
        // char in Java has 2 bytes .
        char ch = 'k';   // Must be in single quotes.
    }
}